    public void TampilData() {
        DftTblModel_langganan = new DefaultTableModel();
        DftTblModel_langganan.addColumn("IDLANGGANAN");
        DftTblModel_langganan.addColumn("NAMA");
        DftTblModel_langganan.addColumn("JENIS KELAMIN");
        DftTblModel_langganan.addColumn("TELEPON");
        DftTblModel_langganan.addColumn("ALAMAT");
        DftTblModel_langganan.addColumn("JENIS TEMPAT");
        jTable_langganan.setModel(DftTblModel_langganan);
        Connection conn = koneksi.getConnection();
        try {
            java.sql.Statement stmt = conn.createStatement();
            SQL = "select * from tb_langganan";
            java.sql.ResultSet res = stmt.executeQuery(SQL);
            while (res.next()) {
                DftTblModel_langganan.addRow(new Object[]{
                    res.getString("IDLANGGANAN"),
                    res.getString("NAMA"),
                    res.getString("JENIS_KELAMIN"),
                    res.getString("TELEPON"),
                    res.getString("ALAMAT"),
                    res.getString("JENIS_TEMPAT")
                });
            }
        } catch (SQLException e) {
            System.out.println(e.getMessage());
        }
    }
    
    

