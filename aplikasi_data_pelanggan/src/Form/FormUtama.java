    private void btn_updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_updateActionPerformed
        // TODO add your handling code here:
        try {
            Connection conn = koneksi.getConnection();
            PreparedStatement stmt = conn.prepareStatement("update tb_langganan set nama=?, jenis_kelamin=?, telepon=?, alamat=?, jenis_tempat=? where idlangganan=?");
            stmt.setString(1, jTextField_id.getText());
            stmt.setString(2, jTextField_nama.getText());
            stmt.setString(3, jComboBox_kelamin.getSelectedItem().toString());
            stmt.setString(4, jTextField_telepon.getText());
            stmt.setString(5, jTextField_alamat.getText());
            stmt.setString(6, jTextField_tempat.getText());
            stmt.executeUpdate();
            DefaultTableModel model = (DefaultTableModel)jTable_langganan.getModel();
            model.setRowCount(0);
            JOptionPane.showMessageDialog(null, "Data berhasil diubah", "Pesan", JOptionPane.INFORMATION_MESSAGE);
            TampilData();
        } catch (SQLException e) {
           JOptionPane.showMessageDialog(null, "Data gagal diubah", "Pesan", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btn_updateActionPerformed

    private void btn_deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_deleteActionPerformed
        // TODO add your handling code here:
        Connection conn = koneksi.getConnection();
             int confirm = JOptionPane.showConfirmDialog(null, "Apakah anda yakin ingin menghapus data tersebut?", "Konfirmasi", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
             if (confirm == 0) {
                 try {
                     java.sql.PreparedStatement stmt = conn.prepareStatement("delete from tb_langganan where idlangganan=' " + jTextField_id.getText()+"'");
                     stmt.executeUpdate();
                     JOptionPane.showMessageDialog(null, "Data berhasil dihapus", "Pesan", JOptionPane.INFORMATION_MESSAGE);
                     TampilData();
                     jTextField_id.setText("");
                     jTextField_nama.setText("");
                     jComboBox_kelamin.setSelectedItem("PILIH :");
                     jTextField_telepon.setText("");
                     jTextField_alamat.setText("");
                     jTextField_tempat.setText("");
                     jTextField_id.requestFocus();
                 } catch (SQLException e) {
                     JOptionPane.showMessageDialog(null, "Data gagal di hapus" + e.getMessage(), "Pesan", JOptionPane.ERROR_MESSAGE);
                 }
        }
    }//GEN-LAST:event_btn_deleteActionPerformed

